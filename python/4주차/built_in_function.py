## 4week Function

'''
내장 함수 (Built-in Function)
'''
# 절대값
print(abs(-3))
# round (반올림)
print(round(1.23))
print(round(1.5))
print(round(1.2345,2))
print(round(1.23451,3))     # 5 부터는 guard 쳬계르 따르는거 같음
print(round(1.9876,3))
# range
print(range(5))
# pow (제곱)
print(pow(2,3))
print(pow(32.4,2))
# len (길이)
print(len("school"))
print(len("학교"))
# min, max
print(max(10,20,30))
print(min(1,2,3,4,5))
# str (문자열로 변환)
a = 2
b = 3
c = a+b 
print(c)
print(str(a)+str(b))
# 정렬 
print(sorted([3,2,1,4]))
